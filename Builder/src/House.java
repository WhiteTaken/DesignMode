/*
 * 房子 可以修建门窗、地板、墙
 */
public class House {
	private String door;
	private String floor;
	private String wall;
	
	public String getDoor() {
		return door;
	}
	public void setDoor(String door) {
		this.door = door;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	public String getWall() {
		return wall;
	}
	public void setWall(String wall) {
		this.wall = wall;
	}	
}
